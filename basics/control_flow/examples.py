# Control Flow Examples

# If statement
temperature = 20
if temperature > 30:
    print("It's a hot day!")
else:
    print("It's not a hot day.")

# For loop
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print("Fruit:", fruit)

# While loop
count = 0
while count < 3:
    print("Count:", count)
    count += 1
