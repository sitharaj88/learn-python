    # Examples of Python Data Types

# Integer
number_of_apples = 5
print("Number of apples:", number_of_apples)

# Float
apple_price = 0.45
print("Price of one apple:", apple_price)

# String
apple_color = "red"
print("Color of the apple:", apple_color)

# Boolean
is_apple_tasty = True
print("Is the apple tasty?", is_apple_tasty)
